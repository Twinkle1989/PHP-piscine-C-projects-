#include <unistd.h>

void ft_putnbr(int nb)
{
	char d;

	if(nb < 0)
	{
		write (1, "-", 1);
		nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr(nb/10);
	}
	d = nb % 10 + '0';
	write(1, &d, 1);
}
int main()
{
	int nb = 4200000;
	ft_putnbr(nb);
}
